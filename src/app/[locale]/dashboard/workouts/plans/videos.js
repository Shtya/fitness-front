// download-images.js
// Node 18+ required (uses built-in fetch)

import { mkdir } from "node:fs/promises";
import { createWriteStream } from "node:fs";
import { pipeline } from "node:stream/promises";
import { basename, dirname, join } from "node:path";
import { setTimeout as delay } from "node:timers/promises";

const OUTPUT_DIR = "public/imgs";
const CONCURRENCY = 4;
const MAX_RETRIES = 3;
const TIMEOUT_MS = 60_000;

// --- Paste your image URLs here ---
const IMAGES = videos.map(v => v).filter(Boolean);

function getFileNameFromUrl(url) {
  const marker = "/GymvisualPNG/";
  const idx = url.indexOf(marker);
  if (idx === -1) return basename(new URL(url).pathname); // fallback
  return decodeURIComponent(url.slice(idx + marker.length));
}

async function downloadToFile(url, filePath, attempt = 1) {
  const controller = new AbortController();
  const t = setTimeout(() => controller.abort(), TIMEOUT_MS);

  try {
    const res = await fetch(url, {
      redirect: "follow",
      signal: controller.signal,
      headers: { "User-Agent": "NodeDownloader/1.0" },
    });
    if (!res.ok) throw new Error(`HTTP ${res.status} ${res.statusText}`);

    await mkdir(dirname(filePath), { recursive: true });
    const fileStream = createWriteStream(filePath);
    await pipeline(res.body, fileStream);
  } catch (err) {
    if (attempt <= MAX_RETRIES) {
      const backoff = 500 * attempt ** 2;
      console.warn(`Retry ${attempt}/${MAX_RETRIES} for ${basename(filePath)} in ${backoff}ms  ${err.message}`);
      await delay(backoff);
      return downloadToFile(url, filePath, attempt + 1);
    }
    throw err;
  } finally {
    clearTimeout(t);
  }
}

async function run() {
  await mkdir(OUTPUT_DIR, { recursive: true });

  const queue = [...IMAGES];
  let active = 0;
  let failed = 0;

  await new Promise((resolve) => {
    const next = () => {
      if (queue.length === 0 && active === 0) return resolve();
      while (active < CONCURRENCY && queue.length) {
        const url = queue.shift();
        active++;

        (async () => {
          const name = getFileNameFromUrl(url);
          const outPath = join(OUTPUT_DIR, name);
          try {
            console.log(`  Downloading: ${name}`);
            await downloadToFile(url, outPath);
            console.log(`  Saved: ${outPath}`);
          } catch (e) {
            failed++;
            console.error(`  ${name}  ${e.message}`);
          } finally {
            active--;
            next();
          }
        })();
      }
    };
    next();
  });

  console.log(failed > 0 ? `Finished with ${failed} failure(s).` : "All images downloaded successfully!");
}

run().catch((e) => {
  console.error("Fatal error:", e);
  process.exit(1);
});

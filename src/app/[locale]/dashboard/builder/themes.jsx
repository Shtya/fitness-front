export function getThemeById(id) {
  return THEME_TEMPLATES.find((t) => t.id === id) || THEME_TEMPLATES[0]
}

export function applyThemeColors(doc, themeId) {
  const theme = getThemeById(themeId)
  return {
    ...doc,
    theme: {
      ...doc.theme,
      ...theme.colors,
    },
  }
}